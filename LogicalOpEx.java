public class LogicalOpEx{
       public static void main(String args[]){
              int n1=30;
              int n2=60;
              System.out.println(n1==n2); 
              System.out.println(n1>n2); 
              System.out.println(n1<n2); 
              System.out.println(n1<=n2); 
              System.out.println(n1>=n2); 
              System.out.println(n1!=n2); 
         
              boolean a=true;
              boolean b=false;
              System.out.println(a&&b); 
              System.out.println(a||b); 
              System.out.println(!a); 
              System.out.println(!b); 
         


}
}