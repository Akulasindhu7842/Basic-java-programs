public class UnaryEx{
       public static void main(String args[]){
              int n1=25;
              System.out.println(n1);
              int n2=n1++;
              System.out.println(n1+" "+n2);
              System.out.println(n2);
              n2=++n1;
              System.out.println(n1+" "+n2);
              char ch1='a';
              char ch2=ch1++;
              System.out.println(ch1+" "+ch2);
}
}