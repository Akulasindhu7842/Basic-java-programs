public class AssignOperatorEx{
       public static void main(String args[]){
              int n1=60;
              System.out.println(n1);
              n1+=5;
              System.out.println(n1);
              n1-=10;
              System.out.println(n1);
              n1*=2;
              System.out.println(n1);
              n1/=10;
              System.out.println(n1);
              n1%=5;
              System.out.println(n1);
     }
}